int		ft_count_if(char **tab, int(*f)(char*))
{
	int		result;
	int		i;
	int		j;

	result = 0;
	i = -1;
	while (tab[++i])
	{
		j = -1;
		while (tab[i][++j])
			if (f(tab[i][j]) == 1)
				++result;
	}
	return (result);
}
