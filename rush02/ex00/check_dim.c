int		get_width(char **tab)
{
	int		j;

	j = 0;
	if (tab[0])
		while (tab[0][j])
			++j;
	return (j - 1);
}

int		get_height(char **tab)
{
	int		i;

	i = 0;
	while (tab[i])
		++i;
	return (i - 1);
}
